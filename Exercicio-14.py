v1 = float(input("Digite um valor: "))
v2 = float(input("Digite outro valor: "))
v3 = float(input("Digite outro valor: "))

if v1 > v2 and v1 > v3:
    print("O primeiro valor é o maior")
elif v2 > v3: 
    print("O segundo valor é o maior")
else:
    print ("o terceiro é o maior") 

       

      






